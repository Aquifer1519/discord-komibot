public class Person {
    String firstName;
    String lastName;
    int age;
    int grade;
    String favoriteSubject;
    public Person(String firstName, String lastName, int age, int grade, String favoriteSubject)    {
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.grade = grade;
        this.favoriteSubject = favoriteSubject;
    }
}
